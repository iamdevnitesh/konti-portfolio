// Auto-generates `data/photos.generated.ts` by scanning `public/photos/<section>/`.
// Each section folder contains image files and an optional `manifest.json` that
// maps filename -> { title, caption, category, date, featured, text }.
//
// Run via `npm run manifest` (also wired to predev / prebuild).

import { readdirSync, readFileSync, writeFileSync, statSync, existsSync } from "node:fs";
import { join, extname, basename } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));
const PHOTOS_DIR = join(ROOT, "public", "photos");
const OUT_FILE = join(ROOT, "data", "photos.generated.ts");

const IMAGE_EXTS = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);
const SECTIONS = ["hero", "featured", "gallery", "closing"];

const slugify = (s) =>
  s
    .toLowerCase()
    .replace(/\.[^.]+$/, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const titleize = (s) =>
  s
    .replace(/\.[^.]+$/, "")
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/\b\w/g, (c) => c.toUpperCase());

const DEFAULT_TEXT = {
  position: { x: "8%", y: "82%" },
  align: "left",
  color: "#f2f2f2",
};

function readManifest(folder) {
  const path = join(folder, "manifest.json");
  if (!existsSync(path)) return { items: {} };
  try {
    const raw = JSON.parse(readFileSync(path, "utf8"));
    return { items: raw.items || {} };
  } catch (err) {
    console.warn(`[manifest] Failed to parse ${path}: ${err.message}`);
    return { items: {} };
  }
}

function listImages(folder) {
  if (!existsSync(folder)) return [];
  return readdirSync(folder)
    .filter((name) => {
      const full = join(folder, name);
      try {
        if (!statSync(full).isFile()) return false;
      } catch {
        return false;
      }
      return IMAGE_EXTS.has(extname(name).toLowerCase());
    })
    .sort();
}

function normalizeText(text) {
  if (!text) return DEFAULT_TEXT;
  return {
    position: {
      x: text.position?.x ?? DEFAULT_TEXT.position.x,
      y: text.position?.y ?? DEFAULT_TEXT.position.y,
    },
    align: text.align ?? DEFAULT_TEXT.align,
    color: text.color ?? DEFAULT_TEXT.color,
  };
}

function buildSection(section) {
  const folder = join(PHOTOS_DIR, section);
  const files = listImages(folder);
  const { items } = readManifest(folder);

  // Warn if manifest references a missing file.
  for (const filename of Object.keys(items)) {
    if (!files.includes(filename)) {
      console.warn(
        `[manifest] ${section}/manifest.json references missing file "${filename}"`
      );
    }
  }

  return files.map((filename, index) => {
    const meta = items[filename] || {};
    const baseSlug = slugify(filename) || `${section}-${index + 1}`;
    return {
      slug: meta.slug || baseSlug,
      filename,
      src: `/photos/${section}/${filename}`,
      title: meta.title || titleize(basename(filename)),
      caption: meta.caption || "",
      category: meta.category || "",
      date: meta.date || "",
      featured: Boolean(meta.featured),
      text: normalizeText(meta.text),
      section,
      order: index,
    };
  });
}

function generate() {
  const data = {};
  for (const section of SECTIONS) {
    data[section] = buildSection(section);
  }

  const header = `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/build-manifest.mjs from public/photos/*/manifest.json.
// Re-run via \`npm run manifest\` (also runs on predev/prebuild).

export type PhotoText = {
  position: { x: string; y: string };
  align: "left" | "center" | "right";
  color: string;
};

export type Photo = {
  slug: string;
  filename: string;
  src: string;
  title: string;
  caption: string;
  category: string;
  date: string;
  featured: boolean;
  text: PhotoText;
  section: "hero" | "featured" | "gallery" | "closing";
  order: number;
};
`;

  const body = SECTIONS.map((section) => {
    const constName = `${section}Photos`;
    return `export const ${constName}: Photo[] = ${JSON.stringify(
      data[section],
      null,
      2
    )};`;
  }).join("\n\n");

  const footer = `

export const allPhotos: Photo[] = [
  ...heroPhotos,
  ...featuredPhotos,
  ...galleryPhotos,
  ...closingPhotos,
];

export function findPhotoBySlug(slug: string): Photo | undefined {
  return allPhotos.find((p) => p.slug === slug);
}
`;

  const out = `${header}\n${body}${footer}`;
  writeFileSync(OUT_FILE, out, "utf8");

  const counts = SECTIONS.map((s) => `${s}=${data[s].length}`).join(", ");
  console.log(`[manifest] wrote ${OUT_FILE} (${counts})`);
}

generate();
